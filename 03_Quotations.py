# we can use the backslash to print quotation marks
message = "\"A diversity of thought, perspective and culture is important in any field, not just engineering. Surround yourself with people who support you and get involved in [coding] programs.\" - Sarah Friar, Chief Financial Officer at Square"
print(message)

# If we want our text to be formatted exactly how we input it, we can use
# triple-quotation marks, similar to a docstring
message = """\"A diversity of thought, perspective and culture is important in any 
field, not just engineering. Surround yourself with people who support
you and get involved in [coding] programs.\"
- Sarah Friar, Chief Financial Officer at Square"""
print(message)